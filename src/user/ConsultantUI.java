    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
       // Validate Consultant ID
    int consultantID;
    try {
        consultantID = Integer.parseInt(JOptionPane.showInputDialog("Enter the Consultant ID"));
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Consultant ID must be a number.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Search for the consultant
    Consultant c = cDB.get(consultantID);
    if (c != null) {
        txtCunsultantID.setText(String.valueOf(c.getcID()));
        txtFirstName.setText(c.getFirstName());
        txtLastName.setText(c.getLastName());
        cmbCategory.setSelectedItem(c.getCategory());
        txtHospital.setText(c.getHospital());
        txtTelephone.setText(c.getTelephone());
    } else {
        JOptionPane.showMessageDialog(pnlConsultant, "The consultant record is not available");
    }
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // Get the consultant ID from the user
    int cID;
    try {
        cID = Integer.parseInt(JOptionPane.showInputDialog("Enter Consultant ID to delete:"));
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Consultant ID must be a number.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Delete the consultant from the database
    boolean success = cDB.delete(cID);

    // Show a message based on the result
    if (success) {
        JOptionPane.showMessageDialog(this, "Consultant deleted successfully!");
    } else {
        JOptionPane.showMessageDialog(this, "Failed to delete consultant.");
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

