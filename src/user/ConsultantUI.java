    private void btnAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAllActionPerformed
        // Clear the table
    tblModel.setRowCount(0);

    // Retrieve all consultants from the database
    ArrayList<Consultant> cList = cDB.getAll();

    // If consultants are found, add them to the table
    if (cList != null) {
        for (Consultant c : cList) {
            tblModel.addRow(new Object[]{
                c.getcID(),
                c.getFirstName(),
                c.getLastName(),
                c.getCategory(),
                c.getHospital(),
                c.getTelephone()
            });
        }
    } else {
        JOptionPane.showMessageDialog(this, "No consultants found.");
    }

    }//GEN-LAST:event_btnAllActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // Validate Consultant ID
    int consultantID;
    try {
        consultantID = Integer.parseInt(txtCunsultantID.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Consultant ID must be a number.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validate First Name and Last Name
    String firstName = txtFirstName.getText();
    String lastName = txtLastName.getText();
    if (firstName.isEmpty() || lastName.isEmpty()) {
        JOptionPane.showMessageDialog(this, "First Name and Last Name are required.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validate Category
    String category = cmbCategory.getSelectedItem().toString();
    if (category.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Category is required.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validate Hospital
    String hospital = txtHospital.getText();
    if (hospital.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Hospital is required.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validate Telephone
    String telephone = txtTelephone.getText();
    if (telephone.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Telephone number is required.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // If all inputs are valid, proceed to add the consultant
    Consultant c = new Consultant(consultantID, firstName, lastName, category, hospital, telephone);
    boolean flag = cDB.add(c);
    if (flag) {
        JOptionPane.showMessageDialog(pnlConsultant, "Consultant Added");
    } else {
        JOptionPane.showMessageDialog(pnlConsultant, "Consultant not Added");
    }
    }//GEN-LAST:event_btnAddActionPerformed

